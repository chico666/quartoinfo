							<p style="text-align: justify">O Ministério da Educação através da Secretaria de Educação Profissional e Tecnológica, criou, em 2009, 38 Institutos Federais de Educação, Ciência e Tecnologia, com objetivo de tornar realidade, em todo o território nacional, o ensino profissional.  
O Instituto Federal de Educação, Ciência e Tecnologia do Rio Grande do Sul tem como objetivo implementar os princípios dos Institutos Federais, subsidiando as políticas de ensino, pesquisa e extensão previstas no seu planejamento, articulado com a sociedade e as demandas regionais. 
Atualmente, são 12 Campi integrantes do Instituto Federal do Rio Grande do Sul, que propõem valorizar a educação em todos os seus níveis, contribuir para com o desenvolvimento do ensino, da pesquisa e da extensão, oportunizar de forma mais expressiva as possibilidades de acesso à educação gratuita e de qualidade e fomentar o atendimento a demandas localizadas, com atenção especial às camadas sociais que carecem de oportunidades de formação e de incentivo à inserção no mundo produtivo.  
O Câmpus Bento Gonçalves integra o Instituto Federal de Educação Ciência e Tecnologia do Rio Grande do Sul. A instituição foi criada em 22 de outubro de 1959, pela Lei nº 3.646, como Colégio de Viticultura e Enologia de Bento Gonçalves, e passou a funcionar de forma efetiva a partir de 27 de março de 1960. Em 25 de março de 1985, alterou sua denominação para Escola Agrotécnica Federal Presidente Juscelino Kubistchek. Em 16 de agosto de 2002, foi implantado o Centro Federal de Educação Tecnológica de Bento Gonçalves (CEFET-BG). 
Em 29 de dezembro de 2008, a Lei 11.892 cria o Instituto Federal de Educação Ciência e Tecnologia do Rio Grande do Sul – IFRS a partir do CEFET-BG, da Escola Agrotécnica Federal de Sertão e da Escola Técnica de Canoas. As escolas técnicas vinculadas as Universidades Federais do Rio Grande do Sul e de Rio Grande (UFRGS e FURG) aderem ao IFRS, ficando o Instituto composto de 12 campi (Bento Gonçalves, Canoas, Caxias do Sul, Erechim, Farroupilha, Feliz, Ibirubá, Osório, Porto Alegre, Restinga, Rio Grande, Sertão).</p>
							
							
